package com.SocketTrench.Engine;

public final class Body {
    public int width;
    public int height;
    public int x;
    public int y;

    public Body(
        final int width,
        final int height,
        final int x,
        final int y
    ) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }
}
