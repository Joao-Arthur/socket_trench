package com.SocketTrench.Engine;

public final class Square {
    public final int x1;
    public final int x2;
    public final int y1;
    public final int y2;

    public Square(
        final int x1,
        final int x2,
        final int y1,
        final int y2
    ) {
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
    }
}
