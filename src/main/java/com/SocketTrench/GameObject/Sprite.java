package com.SocketTrench.GameObject;

public final class Sprite {
    public int width;
    public int height;
    public int x;
    public int y;

    public Sprite(
        int width,
        int height,
        int x,
        int y
    ) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }
}
